    Next time if a cop asks you for your driving license, show him your smartphone. Yes it's that easy. You only need to create an account for your own DigiLocker and you are set to go for a tension free drive.
    The Govt. of India has introduced DigiLocker that is a key initiative under Digital India, wherein Citizens can save and access their digital documents anytime, anywhere and share it online. Users can also digitally sign them using the e-sign facility. These digitally signed documents can be shared with Government organizations or other entities. It can be accessed on desktop using the link https://digilocker.gov.in/ or users can also download an app of DigiLocker. It is a free app and is available on Google Play Store.
    DigiLocker is safe and secure to use. All precautionary measures have been taken to ensure your data is protected and uncompromised.
    Signing up for DigiLocker is easy - all you need is your mobile number. Your mobile number will be authenticated by sending an OTP (one-time password) followed by selecting a username & password. This will create your DigiLocker account. After your DigiLocker account is successfully created, you can upload your important documents for later access.

